#include <libft.h>

void		ft_putnbr_uintmax_t(uintmax_t n)
{
	if (n >= 10)
	{
		ft_putnbr_uintmax_t(n / 10);
		ft_putchar(n % 10 + 48);
	}
	else
		ft_putchar(n + 48);
}
