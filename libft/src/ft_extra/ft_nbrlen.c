#include <libft.h>

size_t		ft_nbrlen(size_t nb)
{
	size_t	i;

	i = 0;
	if (nb <= 0)
	{
		nb = -nb;
		i++;
	}
	while (nb > 0)
	{
		nb = nb / 10;
		i++;
	}
	return (i);
}
