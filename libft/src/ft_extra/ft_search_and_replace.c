#include <libft.h>

void	ft_search_and_replace(char *d, char a, char b)
{
	int i;

	i = 0;
	while (d[i])
	{
		if (d[i] == a)
			write(1, &b, 1);
		else
			write(1, &d[i], 1);
		i++;
	}
}
